package com.example.bean;

public class MeatDo {
	private int price;
	private String name;

	public MeatDo(){

	}
	
	public void setPrice(int price){
		this.price = price;
	}

	public int getPrice(){
		return price;
	}

	public void setName(String name){
		this.name = name;
	}

	public String getName(){
		return name;
	}
}
